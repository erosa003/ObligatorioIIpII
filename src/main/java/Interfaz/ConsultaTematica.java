   private void updateListaTematicas(){
        
        DefaultListModel<String> listaTematicas = new DefaultListModel<>();
        
        for(Tematica t : sistema.getlistaTematicas()){
            listaTematicas.addElement(t.getNombre());
        }
       jList1.setModel(listaTematicas);
   }
    
    
    
